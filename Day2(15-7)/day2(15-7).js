//Promt() method in Javascript...
//The prompt() method is used to display a dialog with an optional message prompting the user to input some text. 
//It is often used if the user wants to input a value before entering a page.
//It returns a string containing the text entered by the user, or null.

//Syntax:
//prompt(title, default)
//title is a string of text to display to the user.It can be omitted if there is nothing to show in the prompt window i.e. it is optional.
//default is a string containing the default value displayed in the text input field. It is also optional

let age = prompt("What is your age?");
console.log(age);
